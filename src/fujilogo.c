void set_screen(byte screenMode)
{
    screen(1, screenMode); // set screen mode
}

void draw_logo(unsigned char *screenBuffer, byte inverted)
{
    // Copy inverted bitmap so 1 = black, 0 = white
    for (int i = 0; i < 6144; i++)
    {
        if (inverted)
        {
            screenBuffer[i] = ~fujinet_bitmap[i];
        }
        else
        {
            screenBuffer[i] = fujinet_bitmap[i];
        }
    }
}

int main(void)
{
    unsigned char *screenBuffer;
    byte ch;
    byte inverted = 0;
    byte screenMode = 1;

    screenBuffer = (byte *) * (byte *) 0x00BC << 8;

    width(32);
    pmode(4, screenBuffer);
    pcls(255);
    set_screen(screenMode);

    // Draw the logo initially
    draw_logo(screenBuffer, inverted);
   
   while(1)
   {
         ch = waitkey(0);
         switch (ch)
         {
            case  'C':
            case  'c':
                 // Change screen mode
                 screenMode = !screenMode;
                 set_screen(screenMode);
                 break;
             case 'Q':
             case 'q':
                 screen(0,0); // restore to normal text mode
                 cls(255);
                 return 0;
                 break;
             case 'R':
             case 'r':
                 // Redraw the logo
                inverted = !inverted;
                draw_logo(screenBuffer, inverted);
                break;
             }  
   }
   return 0;
}
